    public function rescale_rain($param){
        if($param <= 2.94934){
            return 'RENDAH';
        }elseif ($param > 2.94934 && $param <= 5.89719) {
            return 'SEDANG';
        }else{
            return 'TINGGI';
        }
    }
    public function rescale_slope($param){
        if($param < 0.14673){
            return 'LANDAI';
        }elseif ($param > 0.14673 && $param <= 0.21453) {
            return 'SEDANG';
        }else{
            return 'CURAM';
        }
    }
